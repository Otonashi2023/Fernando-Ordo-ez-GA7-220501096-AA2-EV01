    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        int idEquipo = Integer.parseInt(txtCodigo.getText());
        BigInteger serial = new BigInteger(txtSerial.getText());
        
        Equipo e = new Equipo(idEquipo, txtTipo.getText(), txtMarca.getText(), txtModelo.getText(), serial);
        
        CEquipo controladorEquipo = new CEquipo();
        
        controladorEquipo.ModificarEquipo(e);
            
        limpiarPantalla();
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        CEquipo controladorEquipo = new CEquipo();
        controladorEquipo.EliminarEquipo(Integer.parseInt(txtCodigo.getText()));
        limpiarPantalla();
        
    }//GEN-LAST:event_btnEliminarActionPerformed

