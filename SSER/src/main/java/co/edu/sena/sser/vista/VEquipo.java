    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        int cod= Integer.parseInt(txtCodigo.getText());
        BigInteger serial = new BigInteger(txtSerial.getText());
        
        Equipo e = new Equipo(cod, txtTipo.getText(), txtMarca.getText(),txtModelo.getText(), serial);
        
        CEquipo controladorEquipo = new CEquipo();
        
        controladorEquipo.registrarEquipo(e);
        
        limpiarPantalla();
    }//GEN-LAST:event_btnRegistrarActionPerformed

